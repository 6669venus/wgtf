#include "curves_model.h"

bool operator==(const Point& p1, const Point& p2)
{
	return p1.x == p2.x && p1.y == p2.y;
}

Point operator*(float val, const Point& p1)
{
	return p1 * val;
}

Point operator/(float val, const Point& p1)
{
	return p1 / val;
}